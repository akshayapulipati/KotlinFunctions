package com.example.funtionspart1


import java.lang.Math.pow
import kotlin.math.*

fun main(){
    val a = -9.0
    val b = 25.0

    println("sqrt:${sqrt(b)}")
    println("abs: ${abs(a)}")//gives Absolute (positive) value of a
    println("max(10, 20): ${max(10, 20)}")
    println("min(10, 20): ${min(10, 20)}")
    println("pow(2.0, 3.0): ${pow(2.0, 3.0)}")
    println("round(4.6): ${round(4.6)}")//rounds to nearest integer 5
    println("floor(4.9): ${floor(4.9)}")//rounds down to nearst whole num 4.0
    println("ceil(4.1): ${ceil(4.1)}")//rounds up to nearest whole num 5.0

}